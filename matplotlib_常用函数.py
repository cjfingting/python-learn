# Matplotlib 是 Python 的绘图库。 它可与 NumPy 一起使用，提供了一种有效的 MatLab 开源替代方案。 它也可以和图形工具包一起使用，如 PyQt 和 wxPython。
# pyplot的API
# Bar 绘制条形图
# Barh 绘制水平条形图
# Boxplot 绘制一个箱型和须图
# Hist 绘制直方图
# hist2d 绘制2D直方图
# Pie 绘制饼图
# Plot 绘制轴的线/标记
# Polar 绘制一个极坐标图
# Scatter 绘制x与y的散点图
# Stackplot 绘制堆积区域图
# Stem 绘制杆图
# Step 绘制步骤图
# Quiver 绘制二维箭头图

# 图像函数
# Imread 将文件中图像读入数组
# Imsave 像在图像文件中一样保存数组
# Imshow 在轴上显示图像

# 轴函数
# Axes 添加轴到图形
# Text 向轴添加文本
# Title 设置当前轴的标题
# Xlabel 设置当前轴的x轴标签
# Xlim 获取或设置当前轴的x限制
# Xscale 设置x轴的缩放比例
# Xticks 获取或设置当前刻度位置和标签的x限制
# Ylabel 设置当前轴的y轴标签
# Ylim 获取或设置当前轴的y限制
# Yscale 设置y轴的缩放比例
# Yticks 获取或设置当前刻度位置和标签的y限制

# 图形函数
# Figtext 将文字添加到图形
# Figure  创建一个新的图形
# Show    显示一个图形
# Savefig 保存当前图形
# Close   关闭一个图窗口

